        [HttpPost]
        [ValidateAntiForgeryToken]
        [Authorize(Roles = "Admin,HR-Admin")]
        public async Task<IActionResult> Edit(int id, [Bind("CourcesId,CourcesName")] ACourcesPrograms aCourcesName)
        {
            if (id != aCourcesName.CourcesId)
            {
                return NotFound();
            }

            if (ModelState.IsValid)
            {
                try
                {
                    _context.Update(aCourcesName);
                    await _context.SaveChangesAsync();
                }
                catch (DbUpdateConcurrencyException)
                {
                    if (!ACourcesNameExists(aCourcesName.CourcesId))
                    {
                        return NotFound();
                    }
                    else
                    {
                        throw;
                    }
                }
                return RedirectToAction(nameof(Index));
            }
            return View(aCourcesName);
        }

